function [ n_data, x, fx ] = tran09_values ( n_data )

%*****************************************************************************80
%
%% tran09_values() returns some values of the order 9 transportation function.
%
%  Discussion:
%
%    The function is defined by:
%
%      TRAN09(x) = Integral ( 0 <= t <= x ) t^9 * exp(t) / ( exp(t) - 1 )^2 dt
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    19 September 2004
%
%  Author:
%
%    John Burkardt
%
%  Reference:
%
%    Milton Abramowitz and Irene Stegun,
%    Handbook of Mathematical Functions,
%    US Department of Commerce, 1964.
%
%    Allan McLeod,
%    Algorithm 757, MISCFUN: A software package to compute uncommon
%    special functions,
%    ACM Transactions on Mathematical Software,
%    Volume 22, Number 3, September 1996, pages 288-301.
%
%    Stephen Wolfram,
%    The Mathematica Book,
%    Fourth Edition,
%    Wolfram Media / Cambridge University Press, 1999.
%
%  Input:
%
%    integer N_DATA.  The user sets N_DATA to 0 before the first call.  
%    Thereafter, it should simply be the value returned by the previous call.
%
%  Output:
%
%    integer N_DATA.  On each call, the routine increments N_DATA by 1, and
%    returns the corresponding data; when there is no more data, the
%    output value of N_DATA will be 0 again.
%
%    real X, the argument of the function.
%
%    real FX, the value of the function.
%
  n_max = 20;

  fx_vec = [ ...
     0.26469772870084897671E-22, ...
     0.11367943653594246210E-12, ...
     0.74428246255329800255E-08, ...
     0.48022728485415366194E-03, ...
     0.11700243014358676725E+00, ...
     0.27648973910899914391E+01, ...
     0.24716631405829192997E+02, ...
     0.12827119828849828583E+03, ...
     0.46842894800662208986E+03, ...
     0.31673967371627895718E+04, ...
     0.46140886546630195390E+04, ...
     0.11952718545392302185E+05, ...
     0.20001612666477027728E+05, ...
     0.31011073271851366554E+05, ...
     0.10352949905541130133E+06, ...
     0.19743173017140591390E+06, ...
     0.33826030414658460679E+06, ...
     0.36179607036750755227E+06, ...
     0.36360622124777561525E+06, ...
     0.36360880558827162725E+06 ];

  x_vec = [ ...
       0.0019531250E+00, ...
       0.0312500000E+00, ...
       0.1250000000E+00, ...
       0.5000000000E+00, ...
       1.0000000000E+00, ...
       1.5000000000E+00, ...
       2.0000000000E+00, ...
       2.5000000000E+00, ...
       3.0000000000E+00, ...
       4.0000000000E+00, ...
       4.2500000000E+00, ...
       5.0000000000E+00, ...
       5.5000000000E+00, ...
       6.0000000000E+00, ...
       8.0000000000E+00, ...
      10.0000000000E+00, ...
      15.0000000000E+00, ...
      20.0000000000E+00, ...
      30.0000000000E+00, ...
      50.0000000000E+00 ];

  if ( n_data < 0 )
    n_data = 0;
  end

  n_data = n_data + 1;

  if ( n_max < n_data )
    n_data = 0;
    x = 0.0;
    fx = 0.0;
  else
    x = x_vec(n_data);
    fx = fx_vec(n_data);
  end

  return
end
