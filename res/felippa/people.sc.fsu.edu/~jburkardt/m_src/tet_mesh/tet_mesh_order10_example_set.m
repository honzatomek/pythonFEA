%
%    John Burkardt
%
%  Input:
%
%    integer NODE_NUM, the number of nodes.
%
%    integer TETRA_NUM, the number of tetrahedrons.
%
%  Output:
%
%    real NODE_XYZ(3,NODE_NUM), the node coordinates.
%
%    integer TETRA_NODE(10,TETRA_NUM), the nodes forming each tet.
%
  tetra_node(1:10,1:tetra_num) = [ ...
    4,   3,   5,   1,  16,  19,  17,  11,  10,  12; ...
    4,   2,   5,   1,  13,  19,  14,  11,   9,  12; ...
